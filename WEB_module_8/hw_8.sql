-- 5 студентов с наибольшим средним баллом по всем предметам.
SELECT
	student_name, 
	subject_name,
	group_name,
	MAX(avg_grade)
FROM
	(
	SELECT 
		s.student_name AS student_name,
		c.class_name AS subject_name,
		s.group_id AS group_name,
		AVG(g.grade) AS avg_grade 
	FROM students s 
	JOIN grades g 
	ON s.id = g.student_id 
	JOIN classes c 
	ON g.class_id = c.id
	GROUP BY 1,2
	ORDER BY 4 DESC
	)
GROUP BY 2
ORDER BY 4 DESC 
;

